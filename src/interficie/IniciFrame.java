    public void inicia(String usuari) {
        this.usuari = usuari;
        
        // SET Label NOM USUARI
        
        this.jLabelUsuari.setText("Benvingut " + this.usuari);
        
        // SET Llista de PARTIDES de l'Usuari
        
        Vector<String> partides = new Vector<>();
        Vector<String> partides_raw = ctrl_interficie.persist.llistaPartidesUsuari(this.usuari);
        for (String p : partides_raw) {
            String[] parts = p.split("@");
            String nom = parts[1];
            String timestamp = calculaTemps(Long.parseLong(parts[2]));
            String hash = parts[3];
            partides.add(nom + " <" + timestamp + ">");
        }
        this.jList1.setListData(partides);
        
    }
    
    public static String calculaTemps(long seconds) {
            int day = (int)TimeUnit.SECONDS.toDays(seconds);        
            long hours = TimeUnit.SECONDS.toHours(seconds) - (day *24);
            long minute = TimeUnit.SECONDS.toMinutes(seconds) - (TimeUnit.SECONDS.toHours(seconds)* 60);
            long second = TimeUnit.SECONDS.toSeconds(seconds) - (TimeUnit.SECONDS.toMinutes(seconds) *60);
            String tot = "";
            boolean first = true;
            if (day != 0) {tot += (day + "d"); first=false;}
            if (hours != 0) { if(!first) tot += " "; tot += (hours + "h"); first=false;}
            if (minute != 0) { if(!first) tot += " "; tot += (minute + "m"); first=false;}
            if (second != 0) { if(!first) tot += " "; tot += (second + "s"); first=false;}
            if (first) return "no comen√ßada";
            return tot;

    }
    
    
